    private void btnIniciarLeilaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarLeilaoActionPerformed
       Leilao leilao = leiloesList.getSelectedValue();
       if(leilao != null)
       {
            if(leilao instanceof LeilaoManual)
            {   
                if(leilao.getVendedor().equals(facade.getCb().getUsuarioLogado()))
                {
                    if(leilao.getStatus() == Leilao.INICIADO)
                    {
                        JOptionPane.showMessageDialog(null, "Leil찾o est찼 ativo.", "Erro", JOptionPane.ERROR_MESSAGE);
                    }
                    else if(leilao.getStatus() == Leilao.ENCERRADO)
                    {
                        JOptionPane.showMessageDialog(null, "Leil찾o est찼 encerrado.", "Erro", JOptionPane.ERROR_MESSAGE);
                    }
                    else
                    {
                        facade.iniciarLeilao(leilao);
                        JOptionPane.showMessageDialog(null, "Leil찾o iniciado.", "Sistema", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Voc챗 n찾o 챕 o vendedor desse leil찾o.", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
            else
            {
                 JOptionPane.showMessageDialog(null, "Leil찾o n찾o 챕 Manual", "Erro", JOptionPane.ERROR_MESSAGE);
            }
       }
       else
       {
            JOptionPane.showMessageDialog(null, "Leil찾o n찾o selecionado", "Erro", JOptionPane.ERROR_MESSAGE);  
       }
    }//GEN-LAST:event_btnIniciarLeilaoActionPerformed

